package com.wei.ssi.json;

public class Person
{
    String Address;
    String Birthday;

    public String getAddress()
    {
        return Address;
    }
    public void setAddress(String address)
    {
        Address = address;
    }
    public String getBirthday()
    {
        return Birthday;
    }
    public void setBirthday(String birthday)
    {
        Birthday = birthday;
    }
}
